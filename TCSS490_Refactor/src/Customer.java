
public class Customer {
	public String fName;
	public String fStreet;
	public String fZipcode;
	public String fTown;
	public String fCountry;

	public Customer(String name, String street, String zipCode, String town, String country) 
	{
		fName = name;
		fStreet = street;
		fZipcode = zipCode;
		fTown = town;
		fCountry = country;
	}
	
}